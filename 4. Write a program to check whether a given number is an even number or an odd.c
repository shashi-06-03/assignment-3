//4. Write a program to check whether a given number is an even number or an odd
//number without using % operator.
#include<stdio.h>
 int main()
 {
     int x;
     printf("Enter a number\n");
     scanf("%d",&x);
     if(x&&1==1)
        printf("%d is EVEN",x);
     else
        printf("%d is ODD",x);
     return 0;
 }
